public class m1_dataTypes {
    public static void main(String args[]){
        // 1 byte
        byte b = 8; // all data types name should started with small letters
        System.out.println(b);
        
        // 1 byte
        boolean var = false;
        System.out.println(var);

        // 2 byte
        short n = 240;
        System.out.println(n);

        // 2 byte
        char ch = 'a';
        System.out.println(ch);
        
        // 8 byte for double & 4 for float
        double price = 10.5; // extension of float
        System.out.println(price);
        
        // 8 byte for long & 4 for int
        int number = 25;    // extension is long
        System.out.println(number);
        
    }
}
